    <h1>Informatie - Algemeen</h1>
    <p><B><q>Een aap</q></b> bestaat niet. Als je vraagt wat een aap eet, of hoe oud een aap wordt dan kan niemand antwoorden, want er zijn teveel verschillende soorten.</p>

<p>Eigenlijk gaat deze site  over <b>primaten</b>. Dit is de verzamelnaam voor: <u>mensen, mensapen, apen en halfapen</u>. Er zijn meer dan <i>300</i> primaten en 20 daarvan zijn mensapen. Primaten zijn de meest bewegelijke dieren op aarde, ze kunnen lopen, rennen, springen en zwemmen.</p>

<p><b>Mensapen</b> hebben geen staart, apen en halfapen wel. Mensapen en apen zijn slimmer dan halfapen.</p>
<p>Bijna alle apen hebben aan hun handen 5 vingers. De voeten van apen lijken erg op hun handen. Alle mensapen zijn bedreigde diersoorten.</p>
<p>De grootste primaat is de <b>gorilla</b>. Hij kan 200 kilo zwaar worden. De kleinste primaat is de <b>dwergmaki</b>, een halfaap. Hij weegt maar 30 gram: net zoveel als een eetlepel suiker. Hij kan wel hoog springen: maar liefst 5 meter hoog. De kleinste <u>aap</u> is een zijdeaapje. De dwergmaki is maar liefst de helft hiervan.</p>

<h2>Apen</h2>

<p>Apen zijn slimme, speelse zoogdieren.</p>

<p>Ze leven in veel verschillende gebieden: bos, bergen, graslanden en moeras. In Japen leven de Japanse sneeuwapen. Ze kunnen overleven bij -15 graden.</p>

<p>De meeste apen leven in groepen omdat dat veiliger is. Meestal is een mannetje de baas.</p>

<p>Er zijn 130 soorten van waarvan de mandril de grootste is. Hij weegt 50 kilo en hij heeft mooie kleuren in zijn gezicht, deze kleuren worden feller als hij boos, blij of opgewonden is.</p>
<img src="http://static.nationalgeographic.nl/pictures/genjUserPhotoPicture/original/44/13/41/mandril-aap-411344.jpg" width="300">

<p>De baviaan leeft op de grond en loopt op handen en voeten. Ze lopen elke dag wel 5 kilometer om eten te zoeken. Ze zijn sterk en handig waardoor ze andere apen, vogels en zelfs kleine antilopen kunnen vangen.</p>

<img alt="" src="http://static.zoom.nl/64FB1B86812F1E97D56B8D2A97CC5733-dhr--baviaan.jpg" style="width: 200px; height: 150px; " />

<p>Brulapen zijn de luidruchtigste landdieren ter wereld.</p>

<p>Slingerapen hebben een sterke staart die hun hele gewicht kan dragen. De staart is sterker dan de arm. Ze kunnen sprongen maken van maarliefst 20 meter, dat is de lengte van een tennisbaan.</p>

<h3>Mensapen</h3>

<p>Er zijn 5 belangrijke mensapen: Gorilla&#39;s, bonobo&#39;s, chimpansees, orang-oetans en gibbons.</p>

<p>Mensapen houden niet van regen, omdat hun vacht niet waterdicht is. Van boomschors maken ze een soort van paraplu.</p>

<p>De baby&#39;s van mensapen worden rondgedragen tot ze sterk genoeg zijn om zelf te lopen. Een orang-oetan blijft 7 tot 9 jaar bij zijn moeder en een chimpansee ongeveer 5 jaar.</p>

<p>Gorilla&#39;s leven in een grote familie. In een gorilla familie is een mannetje de baas. Gorilla&#39;s krijgen net als mensen grijze haren. Een volwassen mannetje noemen we ook een zilverrug. Jonge mannetjes noemen we zwartruggen. Zij blijven bij de groep tot een jaar of 13, daarna starten ze hun eigen familie. Er kunnen wel meerdere mannetjes in een groep zitten, maar er is er maar 1 de baas. Gorilla&#39;s trommelen op hun borst, als ze opgewonden of ongerust zijn. Het zijn vredige en rustige dieren, maar in 2007 is er in Diergaarde Blijdorp in Rotterdam een gorilla ontsnapt. Zijn naam is Bokito. Tijdens zijn ontsnapping verwondde hij een vrouw. Die vrouw bezocht Bokito 4 keer per week en maakte contact met hem. Waarschijnlijk is hij hierdoor van slag geraakt en werd daardoor agressief.</p>

<img alt="" src="https://upload.wikimedia.org/wikipedia/commons/5/50/Male_gorilla_in_SF_zoo.jpg" style="width: 179px; height: 200px; " />

<p>Chimpansees maken meer dan dertig verschillende geluiden, als ze tevreden zijn maken ze zachte <q>hoehoe</q> geluiden als ze voedsel ontdekken loeien ze en als ze opgewonden zijn gaan ze schreeuwen. Chimpansees zijn agressiever dan andere mensapen. Ze jagen in groepen op kleine en grote dieren en ze eten ook soms andere apen. Ze zijn dol op termieten. Deze vangen ze met stokjes, dan likken ze de stokjes af. Chimpansees leven in Afrika.</p>

<p>Orang-oetans zijn de grootste in boom levende dieren ter wereld. Ze moeten langzaam klimmen om sterke takken te kiezen. Hun naam betekent bosmens. Orang-oetans leven in AziÃ«. Ze leven meestal alleen, als ze elkaar tegenkomen dan negeren ze elkaar.</p>

<p>Bonobo&#39;s werden vroeger dwergchimpansees genoemd. Later werd het als aparte soort erkend. Ze kunnen goed rechtop lopen. bij de meeste primaatgroepen is een mannetje de baas maar bij bonobo&#39;s is een vrouwtje de baas. Bonobo&#39;s leven in Afrika in grote groepen.</p>

<p>Gibbons zijn kleine mensapen. Ze houden van slingeren. Een man en een vrouw blijven voor altijd bij elkaar. De gibbons leven in AziÃ«.</p>

<h2>Halfapen</h2>

<p>Een andere naam voor Halfaap is maki. Halfapen kunnen geen kleuren zien, ze vertrouwen meer op hun neus dan op hun ogen. Ze leven op Madagaskar, dat is een eiland vlak bij Afrika. Miljoenen jaren geleden is dat eiland van het vaste land afgebroken. Hierdoor zijn de apen zich anders gaan ontwikkelen. Er komen daar soorten voor die nergens anders voorkomen. Er zijn ongeveer 40 soorten maki&#39;s.</p>

<p>Ringstaartmaki&#39;s zijn de bekendste halfapen. Z zijn goede klimmers en lopen als katten over de grond. Ze leven in groeten groepen en een vrouwtje is de baas. Hun gestreepte staart is langer dan kop en lichaam samen.</p>

