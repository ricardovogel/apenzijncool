<h1>Bedreigde primaten</h1>

<p>Bijna alle primaten zouden over twintig jaar zijn uitgestorven als de mensen niets doen en de bossen blijven kappen. Dankzij natuurbeschermers is in de laatste honderd jaar maar 1 primaat uitgestorven. De Braziliaanse gouden leeuwaapjes zijn gered doordat hun bossen in Brazili&#235; beschermd werden.</p>

<p>Kleine boomapen leven in constant gevaar. Als ze verkeerd slingeren en uit de boom vallen, kunnen ze aangevallen worden door gronddieren. Als ze te hoog in de boom zitten kunnen ze aangevallen worden door roofdieren (adelaars) en verder zijn er nog de boomslangen waar ze op moeten letten.

Ander vijanden zijn krokodillen, luipaarden, pythons en chimpansees.

Sommige apen worden door mensen gevangen voor hun vlees en vacht.</p>


